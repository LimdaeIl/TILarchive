export const perfectScore = 100;

export const sum = (number1, number2) => {
    return number1 + number2;
};

export const avg = (num1, num2) => {
    return (num1 + num2) / 2;
};

const substract = (num1, num2) => {
    return num1 - num2;
}

export default { 
    perfectScore,
    sum,
    avg,
    substract
};
